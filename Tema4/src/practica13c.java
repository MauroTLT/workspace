public class practica13c {
	public void burbuja(int[] array){
		for(int i = 1; i < array.length; i++){
			for(int j = 0;j < array.length-i;j++){
				if(array[j] > array[j+1]){
					int auxiliar = array[j];
					array[j] = array[j+1];
					array[j+1] = auxiliar;
				}
			}
		System.out.println(Arrays.toString(array));
		}
    }
}