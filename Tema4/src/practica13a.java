public class practica13a {
	public void insertionSort (int[] array) {
		 int i= 0;
		 int j= 0;
		 int a= 0;
		 for (i = 1; i < array.length; i++) {
		     a = array[i];
		     for (j = i - 1; j >=0 && array[j] > a; j--){
		          array[j + 1] = array[j];
		     }
		     array[j + 1] = a;
		     System.out.println(Arrays.toString(array));
		 }
	}
}